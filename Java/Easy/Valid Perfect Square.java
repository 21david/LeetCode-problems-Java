class Solution {
    public boolean isPerfectSquare(int num) {
        if(num == 0)
            return false;
        
        double sqrt = Math.sqrt(num);
        double sqrtInt = (int) Math.sqrt(num);
        
        if(sqrt - sqrtInt == 0)
            return true;
        else
            return false;
    }
}
